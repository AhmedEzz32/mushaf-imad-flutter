/// This avoids needing to parse the Realm database for basic navigation.

/// Chapter metadata for the 114 surahs.
class ChapterData {
  final int number;
  final String arabicTitle;
  final String englishTitle;
  final int versesCount;
  final int startPage;
  final bool isMeccan;

  const ChapterData({
    required this.number,
    required this.arabicTitle,
    required this.englishTitle,
    required this.versesCount,
    required this.startPage,
    required this.isMeccan,
  });
}

/// Page metadata.
class PageData {
  final int pageNumber;
  final int juz;
  final List<int> chapterNumbers; // chapters appearing on this page

  const PageData({
    required this.pageNumber,
    required this.juz,
    required this.chapterNumbers,
  });
}

